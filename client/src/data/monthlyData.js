// Helper functions to work with the data
export const getMonthData = (monthNumber) => {
  return monthlySummaryData.find(data => data.month === monthNumber);
};

export const getMonthDataByName = (monthName) => {
  return monthlySummaryData.find(data => 
    data.monthName.toLowerCase() === monthName.toLowerCase()
  );
};

export const getAvailableMonths = () => {
  return monthlySummaryData.map(data => ({
    value: data.monthName.toLowerCase(),
    label: `${data.monthName} ${data.year}`,
    hasData: true,
    monthNumber: data.month
  }));
};

export const processChartData = (monthData) => {
  if (!monthData) return null;

  return {
    attendanceData: monthData.dailyAttendance.map(day => ({
      day: day.day.substring(0, 3), // Short day names
      present: day.present,
      absent: day.absent,
      total: day.totalEmployees,
      attendanceRate: day.attendanceRate
    })),
    departmentData: monthData.departmentBreakdown.map((dept, index) => ({
      name: dept.departmentName,
      value: dept.employeeCount,
      percentage: ((dept.employeeCount / monthData.totalEmployees) * 100).toFixed(1),
      color: ['#3b82f6', '#10b981', '#f59e0b', '#ef4444', '#8b5cf6'][index % 5]
    })),
    leaveData: monthData.weeklyData.map(week => ({
      week: week.weekLabel,
      sick: Math.floor(monthData.leaveSummary.sickLeave / 4), // Distribute across weeks
      casual: Math.floor(monthData.leaveSummary.casualLeave / 4),
      annual: Math.floor(monthData.leaveSummary.annualLeave / 4)
    })),
    salaryData: monthData.departmentBreakdown.map(dept => ({
      department: dept.departmentName,
      avgSalary: dept.avgSalary
    })),
    monthlyTrendData: monthData.weeklyData
  };
};

export const calculatePreviousMonthComparison = (currentMonth, metric) => {
  const currentMonthNumber = currentMonth.month;
  const previousMonthData = getMonthData(currentMonthNumber - 1);
  
  if (!previousMonthData) return null;
  
  const currentValue = currentMonth[metric];
  const previousValue = previousMonthData[metric];
  
  if (previousValue === 0) return null;
  
  const changePercent = ((currentValue - previousValue) / previousValue * 100).toFixed(1);
  return {
    change: changePercent,
    isPositive: changePercent > 0,
    previousValue,
    currentValue
  };
};